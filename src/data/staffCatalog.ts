/** 라벨에서 주기/시간 접두어를 떼어 ROUTINE_GUIDE 키로 정규화 (옛 라벨 호환) */
function normalizeRoutineKey(label: string): string {
  return label
    .replace(/^(매일|매주|매월|주간|실시간|주문\s*시|상품\s*등록\s*시)\s*/, '')
    .replace(/^\d{1,2}:\d{2}\s*/, '')
    .trim();
}

/** 일과 라벨 → 설명 (없으면 undefined). 옛 라벨(시간 포함)도 자동 매칭. */
export function routineGuide(label: string): string | undefined {
  return ROUTINE_GUIDE[label] ?? ROUTINE_GUIDE[normalizeRoutineKey(label)];
}

export function getStaffType(key: string): StaffTypeDef | undefined {
  return STAFF_TYPES.find(t => t.key === key);
}
